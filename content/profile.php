                        <p align="justify">Kata perpustakaan berasal dari kata pustaka, yang berarti: (1) kitab, buku-buku, (2) kitab primbon. 
                        Kemudian kata pustaka mendapat awalan per dan akhiran an, menjadi perpustakaan. Perpustakaan mengandung arti: 
                        (1) kumpulan buku-buku bacaan, (2) bibliotek, dan (3) buku-buku kesusastraan (Kamus Besar Bahasa Indonesia-KBBI).</p>
                        <p align="justify">Perpustakaan digital (Inggris: digital library atau electronic library atau virtual library) adalah perpustakaan yang 
                        mempunyai koleksi buku sebagian besar dalam bentuk format digital dan yang bisa diakses dengan komputer. Jenis perpustakaan 
                        ini berbeda dengan jenis perpustakaan konvensional yang berupa kumpulan buku tercetak, film mikro (microform dan microfiche), 
                        ataupun kumpulan kaset audio, video, dll. Isi dari perpustakaan digital berada dalam suatu komputer server yang bisa ditempatkan 
                        secara lokal, maupun di lokasi yang jauh, namun dapat diakses dengan cepat dan mudah lewat jaringan komputer.</p>
